            lcd_set_pixel ( xk - 1, yk, 1 );
            lcd_set_pixel ( xk + 1, yk, 1 );
            lcd_set_pixel ( xk, yk - 1, 1 );
            lcd_set_pixel ( xk, yk, 1 );
            lcd_set_pixel ( xk, yk + 1, 1 );
            lcd_to_screen (  );
            lcd_to_screen (  );
            ja = car_check_wall ( xm, ym, xe, ye, f1_maps );
            if ( ja == 1 )
            {
                die (  );
                return;
            }
            if ( ja == 2 )
            {
                win (  );
                return;
            }
            sleep2 ( 50 );
            lcd_set_pixel ( xk - 1, yk - 1, 0 );
            lcd_set_pixel ( xk + 1, yk - 1, 0 );
            lcd_set_pixel ( xk - 1, yk + 1, 0 );
